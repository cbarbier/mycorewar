#include "corewar.h"
	
static int	put_arena(t_vm *vm)
{
	int		i;
	int		j;

	j = 0;
	while(j < 64)
	{
		ft_printf("0x%.4x :", j * 64);
		i = 0;
		while (i < 64)
			ft_printf(" %.2x", vm->arena[64 * j + i++].i);
		j++;
		ft_printf("\n");
	}
	return (1);
}
